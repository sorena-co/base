    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        ClassGroupDTO classGroupDTO = (ClassGroupDTO) o;
        if (classGroupDTO.getId() == null || getId() == null) {
            return false;
        }
        return Objects.equals(getId(), classGroupDTO.getId());
    }

